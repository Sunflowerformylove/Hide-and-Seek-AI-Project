from random import randint, shuffle


class Map:
    def __init__(self, N: int, M: int, maze: list[list[int]], obstacles: list[list[int]]):
        self.N = N
        self.M = M
        self.maze = maze
        self.obstacles = obstacles

    def export_map(self, file: str) -> None:
        f = open(file, "w+")
        f.write(str(self.N) + " " + str(self.M) + "\n")

        for i in range(self.N):
            for j in range(self.M):
                f.write(str(self.maze[i][j]))
                if j != self.M - 1:
                    f.write(" ")
            if self.obstacles is not None:
                f.write("\n")
            else:
                if i != self.N - 1:
                    f.write("\n")

        if self.obstacles is not None:
            for obstacle in self.obstacles:
                for i in range(4):
                    f.write(str(obstacle[i]))
                    if i != 3:
                        f.write(" ")
                if self.obstacles.index(obstacle) != len(self.obstacles) - 1:
                    f.write("\n")
        f.close()
        return file


def generate_hider_and_seeker_positions(map: list[list[int]], N: int, M: int, num_hiders: int) -> None:
    cnt_hiders = 0
    cnt_seeker = 0
    is_zone1 = False
    is_zone2 = False
    is_zone3 = False
    is_zone4 = False

    def is_in_cage(map: list[list[int]], N: int, M: int, x: int, y: int) -> bool: # check if the hider/seeker is in the cage made by walls or not
        if x == 0:
            if y == 0:
                return map[x+1][y+1] == 1 and map[x][y+1] == 1 and map[x+1][y] == 1
            elif y == M - 1:
                return map[x+1][y-1] == 1 and map[x][y-1] == 1 and map[x+1][y] == 1
            else:
                return map[x+1][y-1] == 1 and map[x+1][y+1] == 1 and map[x+1][y] == 1 and map[x][y-1] == 1 and map[x][y+1] == 1
        elif x == N - 1:
            if y == 0:
                return map[x-1][y+1] == 1 and map[x][y+1] == 1 and map[x-1][y] == 1
            elif y == M - 1:
                return map[x-1][y-1] == 1 and map[x][y-1] == 1 and map[x-1][y] == 1
            else:
                return map[x-1][y-1] == 1 and map[x-1][y+1] == 1 and map[x-1][y] == 1 and map[x][y-1] == 1 and map[x][y+1] == 1
        else:
            if y == 0:
                return map[x-1][y+1] == 1 and map[x+1][y+1] == 1 and map[x][y+1] == 1 and map[x-1][y] == 1 and map[x+1][y] == 1
            elif y == M - 1:
                return map[x-1][y-1] == 1 and map[x+1][y-1] == 1 and map[x][y-1] == 1 and map[x-1][y] == 1 and map[x+1][y] == 1
            else:
                return map[x-1][y-1] == 1 and map[x-1][y+1] == 1 and map[x+1][y-1] == 1 and map[x+1][y+1] == 1 and map[x-1][y] == 1 and map[x+1][y] == 1 and map[x][y-1] == 1 and map[x][y+1] == 1

    # generate hiders positions
    while cnt_hiders < num_hiders:
        zone = randint(1, 4)
        if zone == 1:
            x = randint(0, N//2 - 1)
            y = randint(0, M//2 - 1)
            if map[x][y] == 0 and not is_in_cage(map, N, M, x, y):
                map[x][y] = 2
                cnt_hiders += 1
                is_zone1 = True
        elif zone == 2:
            x = randint(0, N//2 - 1)
            y = randint(M//2 + 1, M-1)
            if map[x][y] == 0 and not is_in_cage(map, N, M, x, y):
                map[x][y] = 2
                cnt_hiders += 1
                is_zone2 = True
        elif zone == 3:
            x = randint(N//2 + 1, N-1)
            y = randint(0, M//2 - 1)
            if map[x][y] == 0 and not is_in_cage(map, N, M, x, y):
                map[x][y] = 2
                cnt_hiders += 1
                is_zone3 = True
        else:
            x = randint(N//2 + 1, N-1)
            y = randint(M//2 + 1, M-1)
            if map[x][y] == 0 and not is_in_cage(map, N, M, x, y):
                map[x][y] = 2
                cnt_hiders += 1
                is_zone4 = True

    # generate seeker position
    while cnt_seeker < 1:
        if is_zone1 == False: # seeker will be in zone 1 of zone 1
            x = randint(0, N//4 - 1)
            y = randint(0, M//4 - 1)
        elif is_zone2 == False: # seeker will be in zone 2 of zone 2
            x = randint(0, N//4 - 1)
            y = randint(3*M//4 + 1, M-1)
        elif is_zone3 == False: # seeker will be in zone 3 of zone 3
            x = randint(3*N//4 + 1, N-1)
            y = randint(0, M//4 - 1)
        elif is_zone4 == False: # seeker will be in zone 4 of zone 4
            x = randint(3*N//4 + 1, N-1)
            y = randint(3*M//4 + 1, M-1)
        else: # if all zones have hiders then the seeker must be in the center of the map
            x = N // 2
            y = M // 2
            while map[x][y] != 0:
                x += randint(-1, 1)
                y += randint(-1, 1)

        if map[x][y] == 0 and not is_in_cage(map, N, M, x, y):
            map[x][y] = 3
            cnt_seeker += 1


def generate_map_randomly(N: int, M: int, num_hiders: int, type_map: int) -> Map: # N is row, M is column
    map = [[0 for i in range(M)] for j in range(N)]

    if type_map == 1: # 1: discrete walls
        create_discrete_walls(map, N, M)
    elif type_map == 2: # 1: L-shape walls
        create_L_walls(map, N, M)
    else:
        return None

    generate_hider_and_seeker_positions(map, N, M, num_hiders)

    return Map(N, M, map, None)


def create_discrete_walls(map: list[list[int]], N: int, M: int) -> None:
    num_walls = N*M // 10 + randint(1,2)
    cnt_walls = 0
    max_continous_walls = (N+M) // 10
    cnt_continous_walls = 0

    def check_diagonal_wall(map: list[list[int]], N: int , M: int, x: int, y: int) -> bool:
        if x == 0:
            if y == 0:
                return map[x+1][y+1] == 1
            elif y == M - 1:
                return map[x+1][y-1] == 1
            else:
                return map[x+1][y-1] == 1 or map[x+1][y+1] == 1
        elif x == N - 1:
            if y == 0:
                return map[x-1][y+1] == 1
            elif y == M - 1:
                return map[x-1][y-1] == 1
            else:
                return map[x-1][y-1] == 1 or map[x-1][y+1] == 1
        else:
            if y == 0:
                return map[x-1][y+1] == 1 or map[x+1][y+1] == 1
            elif y == M - 1:
                return map[x-1][y-1] == 1 or map[x+1][y-1] == 1
            else:
                return map[x-1][y-1] == 1 or map[x-1][y+1] == 1 or map[x+1][y-1] == 1 or map[x+1][y+1] == 1

    while cnt_walls < num_walls:
        x = randint(0, N-1)
        y = randint(0, M-1)

        if map[x][y] == 0:
            # to avoid creating diagonal walls
            if check_diagonal_wall(map, N, M, x, y):
                continue
            
            if (x != 0 and map[x-1][y] == 1 ) or (x != N-1 and map[x+1][y] == 1) or (y != 0 and map[x][y-1] == 1) or (y != M-1 and map[x][y+1] == 1):
                if cnt_continous_walls < max_continous_walls:
                    map[x][y] = 1
                    cnt_walls += 1
                    cnt_continous_walls += 1
            else:
                map[x][y] = 1
                cnt_walls += 1


def create_L_walls(map_: list[list[int]], N: int, M: int) -> None:
    # Function to check if given coordinates are within map boundaries
    def is_valid(x, y):
        return 0 <= x < N and 0 <= y < M

    # Function to check if a wall can be placed at given coordinates
    def can_place_wall(x, y):
        return is_valid(x, y) and map_[x][y] == 0

    # Function to check if the placement creates a diagonal wall
    def is_diagonal(peak1, peak2, peak3): # 3 tuples representing positions of three peaks of the L-shape wall
        if is_valid(peak1[0]-1,peak1[1]-1) and is_valid(peak1[0]-1,peak1[1]+1):
            if map_[peak1[0]-1][peak1[1]-1] == 1 and map_[peak1[0]][peak1[1]-1] != 1 and map_[peak1[0]-1][peak1[1]] != 1:
                return True
            if map_[peak1[0]-1][peak1[1]+1] == 1 and map_[peak1[0]][peak1[1]+1] != 1 and map_[peak1[0]-1][peak1[1]] != 1:
                return True
        if is_valid(peak2[0]+1, peak2[1]-1): 
            if map_[peak2[0]+1][peak2[1]-1] == 1 and map_[peak2[0]][peak2[1]-1] != 1 and map_[peak2[0]+1][peak2[1]] != 1:
                return True
        if is_valid(peak3[0]+1, peak3[1]+1):
            if map_[peak3[0]+1][peak3[1]+1] == 1 and map_[peak3[0]][peak3[1]+1] != 1 and map_[peak3[0]+1][peak3[1]] != 1:
                return True
            if map_[peak3[0]-1][peak3[1]+1] == 1 and map_[peak3[0]][peak3[1]+1] != 1 and map_[peak3[0]-1][peak3[1]] != 1:
                return True
        return False

    # Shuffle the order of cells to place walls randomly
    cells = [(i, j) for i in range(N) for j in range(M)]
    shuffle(cells)

    max_walls = (N + M) // 2 + max(N, M) // 10
    num_walls = 0
    while num_walls < max_walls:
        long_edge_size = randint(2, 3)
        short_edge_size = randint(1, 2)
        for i, j in cells:
            if long_edge_size == 3:
                if short_edge_size == 1:
                    if can_place_wall(i, j) and can_place_wall(i+1, j) and can_place_wall(i+2, j) and can_place_wall(i+3, j) and can_place_wall(i+3, j+1) and not is_diagonal((i,j), (i+3,j), (i+3,j+1)):
                        map_[i][j] = 1
                        map_[i+1][j] = 1
                        map_[i+2][j] = 1
                        map_[i+3][j] = 1
                        map_[i+3][j+1] = 1
                        break
                elif short_edge_size == 2:
                    if can_place_wall(i, j) and can_place_wall(i+1, j) and can_place_wall(i+2, j) and can_place_wall(i+3, j) and can_place_wall(i+3, j+1) and can_place_wall(i+3, j+2) and not is_diagonal((i,j), (i+3,j), (i+3,j+2)):
                        map_[i][j] = 1
                        map_[i+1][j] = 1
                        map_[i+2][j] = 1
                        map_[i+3][j] = 1
                        map_[i+3][j+1] = 1
                        map_[i+3][j+2] = 1
                        break
            elif long_edge_size == 2:
                if short_edge_size == 1:
                    if can_place_wall(i, j) and can_place_wall(i+1, j) and can_place_wall(i+2, j) and can_place_wall(i+2, j+1) and not is_diagonal((i,j), (i+2,j), (i+2,j+1)):
                        map_[i][j] = 1
                        map_[i+1][j] = 1
                        map_[i+2][j] = 1
                        map_[i+2][j+1] = 1
                        break
                elif short_edge_size == 2:
                    if can_place_wall(i, j) and can_place_wall(i+1, j) and can_place_wall(i+2, j) and can_place_wall(i+2, j+1) and can_place_wall(i+2, j+2) and not is_diagonal((i,j), (i+2,j), (i+2,j+2)):
                        map_[i][j] = 1
                        map_[i+1][j] = 1
                        map_[i+2][j] = 1
                        map_[i+2][j+1] = 1
                        map_[i+2][j+2] = 1
                        break
        num_walls += 1


